/**
 * ip字符串转换为数值
 * @param ipstr ip字符串
 * @returns {Number}
 */
function ip2long(ipstr) {
  var ip = 0;
  var ipdot = ipstr.split('.').reverse();
  var ipis = ipdot.map(function (v, i) {
    return v * Math.pow(256, i);
  });
  ipis.forEach(function (v, i) {
    return ip += v;
  });
  return ip;
};
/**
 * ip数值转换为字符串
 * @param ip ip数值
 * @returns {String}
 */
function long2ip(ip) {
  var ipis = [0, 0, 0, 0];
  var ipdots = ipis.map(function (v, i) {
    var n = Math.pow(256, 3 - i);
    var ipdot = parseInt(ip / n);
    ip = ip % n;
    return ipdot;
  });
  return ipdots.join('.');
}
/**
 * check if this ip is invalid
 * @param ip
 */
function isIp(ip) {
  var array = ip.split('.');
  if (array.length !== 4) {
    throw new Error('IP IS INVALID');
  }
  for (var i = 0; i < 4; i+=1) {
    if(parseInt(array[i])>255 || parseInt(array[i])<0) {
      throw new Error('IP IS INVALID');
    }
  }
}
